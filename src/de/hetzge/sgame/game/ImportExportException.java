package de.hetzge.sgame.game;

public class ImportExportException extends Exception {

	public ImportExportException() {
		super();
	}

	public ImportExportException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ImportExportException(String arg0) {
		super(arg0);
	}

	public ImportExportException(Throwable arg0) {
		super(arg0);
	}

}
